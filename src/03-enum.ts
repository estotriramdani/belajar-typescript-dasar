// numeric enums
enum Incremental {
  ONE,
  TWO = 2,
  THREE,
  FOUR,
  FIVE,
}

console.log(Incremental.ONE);
console.log(Incremental.TWO);
console.log(Incremental.THREE);

// sting enums
enum Month {
  JAN = 'Januari',
  FEB = 'Februari',
  MAR = 'MARET',
  APR = 122,
}

console.log(Month.JAN);
console.log(Month.APR);
